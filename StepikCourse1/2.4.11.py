first_number = int(input())
second_number = int(input())

sum_of_numbers = first_number + second_number
difference_of_numbers = first_number - second_number
product_of_numbers = first_number * second_number

print(first_number, '+', second_number, '=', sum_of_numbers)
print(first_number, '-', second_number, '=', difference_of_numbers)
print(first_number, '*', second_number, '=', product_of_numbers)
